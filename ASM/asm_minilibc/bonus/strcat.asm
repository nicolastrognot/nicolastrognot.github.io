	mov	al, byte[rsi]		;
	mov	byte[rdi], al		;
	cmp	byte[rsi], 0h		;
	je	strcat_end		;
	inc	rdi			;
	inc	rsi			;
	jmp	strcat_begin		;
strcat_end:				;
	lea	rax, [r10]		;
